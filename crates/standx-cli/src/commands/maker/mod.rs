/// Pending place awaiting order-id adoption in live mode. The HTTP request is
/// correlated to its asynchronous response by request ID, then to the visible
/// open order by client order ID. Side, price, and quantity remain only as a
/// compatibility fallback for orders submitted before client IDs were added.
struct PendingPlace {
    request_id: String,
    cl_ord_id: String,
    side: OrderSide,
    price: f64,
    qty: f64,
    level: u32,
    ref_center: f64,
    cycle: u64,
}

/// Whether an exchange order was created by this maker command.
fn is_maker_order(order: &Order) -> bool {
    order
        .cl_ord_id
        .as_deref()
        .is_some_and(|id| id.starts_with(MAKER_CL_ORD_ID_PREFIX))
}

fn is_current_run_order(order: &Order, run_order_prefix: &str) -> bool {
    order
        .cl_ord_id
        .as_deref()
        .is_some_and(|id| id.starts_with(run_order_prefix))
}

fn position_for_symbol(positions: &[Position], symbol: &str) -> Result<f64> {
    positions
        .iter()
        .filter(|position| position.symbol.eq_ignore_ascii_case(symbol))
        .try_fold(0.0, |total, position| {
            let qty = position.qty.parse::<f64>().map_err(|_| {
                anyhow::anyhow!("position on {symbol} has invalid qty '{}'", position.qty)
            })?;
            if !qty.is_finite() {
                return Err(anyhow::anyhow!(
                    "position on {symbol} has invalid non-finite qty"
                ));
            }
            // Venue responses have used both unsigned quantities with an
            // explicit side and signed quantities with that same side. Treat
            // side as authoritative whenever it is present so a short such
            // as `side=sell, qty=-0.13` remains a short rather than failing
            // reconciliation (or being double-negated).
            let signed_qty = match position.side {
                Some(OrderSide::Sell) => -qty.abs(),
                Some(OrderSide::Buy) => qty.abs(),
                None => qty,
            };
            Ok(total + signed_qty)
        })
}

fn starting_position_within_limit(position: f64, max_position: f64, qty_decimals: u32) -> bool {
    let qty_tolerance = 10_f64.powi(-(qty_decimals as i32)) / 2.0;
    position.abs() <= max_position + qty_tolerance
}

/// A submitted placement reserves its `(side, level)` until it is either
/// rejected, visible as an open order, or times out. This stops polling lag
/// from turning one desired quote into multiple real orders.
fn pending_covers_slot(pending: &[PendingPlace], side: OrderSide, level: u32) -> bool {
    pending
        .iter()
        .any(|place| place.side == side && place.level == level)
}

fn apply_order_responses(
    receiver: &mut tokio::sync::mpsc::Receiver<OrderResponse>,
    pending: &mut Vec<PendingPlace>,
    output_format: OutputFormat,
    symbol: &str,
    cycle: u64,
    price_decimals: u32,
) -> Result<()> {
    loop {
        let response = match receiver.try_recv() {
            Ok(response) => response,
            Err(tokio::sync::mpsc::error::TryRecvError::Empty) => return Ok(()),
            Err(tokio::sync::mpsc::error::TryRecvError::Disconnected) => {
                return Err(anyhow::anyhow!(
                    "order-response stream disconnected; refusing further live orders"
                ));
            }
        };
        let Some(request_id) = response.request_id.as_deref() else {
            continue;
        };
        let Some(index) = pending
            .iter()
            .position(|place| place.request_id == request_id)
        else {
            // Authentication acknowledgements and cancellation responses do
            // not correspond to a pending maker place.
            continue;
        };

        if response.accepted() {
            continue;
        }

        let rejected = pending.remove(index);
        output::log_maker_event(
            output_format,
            symbol,
            cycle,
            "place_rejected_async",
            rejected.side,
            rejected.level,
            rejected.price,
            price_decimals,
            &response.message,
        );
    }
}

#[allow(clippy::too_many_arguments)]
fn apply_account_events(
    receiver: &mut tokio::sync::mpsc::Receiver<AccountEvent>,
    ledger: &mut MakerLedger,
    stats: &mut MakerStats,
    symbol: &str,
    run_order_prefix: &str,
    mark: f64,
    cycle: u64,
    output_format: OutputFormat,
) -> Result<(u64, Option<f64>)> {
    let mut fills_total = 0;
    let mut latest_position = None;
    loop {
        let event = match receiver.try_recv() {
            Ok(event) => event,
            Err(tokio::sync::mpsc::error::TryRecvError::Empty) => {
                return Ok((fills_total, latest_position));
            }
            Err(tokio::sync::mpsc::error::TryRecvError::Disconnected) => {
                return Err(anyhow::anyhow!("authenticated account stream disconnected"));
            }
        };
        let (fills, position) = apply_account_event(
            event,
            ledger,
            stats,
            symbol,
            run_order_prefix,
            mark,
            cycle,
            output_format,
        )?;
        fills_total += fills;
        if position.is_some() {
            latest_position = position;
        }
    }
}

#[allow(clippy::too_many_arguments)]
fn apply_account_event(
    event: AccountEvent,
    ledger: &mut MakerLedger,
    stats: &mut MakerStats,
    symbol: &str,
    run_order_prefix: &str,
    mark: f64,
    cycle: u64,
    output_format: OutputFormat,
) -> Result<(u64, Option<f64>)> {
    match event {
        AccountEvent::Connected { .. } => Ok((0, None)),
        AccountEvent::Order(update) => {
            let mut fills = Vec::new();
            ledger.apply_order_update(
                &update,
                symbol,
                run_order_prefix,
                mark,
                stats,
                &mut fills,
            )?;
            for fill in &fills {
                emit_live_fill(fill, symbol, cycle, output_format);
            }
            Ok((fills.len() as u64, None))
        }
        AccountEvent::Position(update) => {
            if !update.symbol.eq_ignore_ascii_case(symbol) {
                return Ok((0, None));
            }
            let qty = update.qty.parse::<f64>().map_err(|_| {
                anyhow::anyhow!("account position update has invalid qty '{}'", update.qty)
            })?;
            if !qty.is_finite() {
                return Err(anyhow::anyhow!(
                    "account position update has non-finite qty"
                ));
            }
            Ok((0, Some(qty)))
        }
        AccountEvent::TradeShadow { seq, data } => {
            if output_format == OutputFormat::Json {
                println!(
                    "{}",
                    serde_json::json!({
                        "ts": chrono::Utc::now().to_rfc3339_opts(chrono::SecondsFormat::Secs, true),
                        "symbol": symbol,
                        "cycle": cycle,
                        "action": "account_trade_shadow",
                        "seq": seq,
                        "data": data,
                    })
                );
            }
            Ok((0, None))
        }
        AccountEvent::Disconnected { reason } | AccountEvent::Error { reason } => Err(
            anyhow::anyhow!("authenticated account stream unhealthy: {reason}"),
        ),
    }
}

