/// Env var gating live order placement. The live path ships code-complete but
/// locked until it has been supervised-tested against production.
const LIVE_MAKER_ENV: &str = "STANDX_ENABLE_LIVE_MAKER";
/// Prefix reserved for orders generated by this maker command. Never use it
/// for hand-entered orders: the maker may cancel any order bearing this tag.
const MAKER_CL_ORD_ID_PREFIX: &str = "sxmk-";

/// Why the maker loop stopped.
enum MakerExit {
    CtrlC,
    /// The asynchronous order-response stream cannot confirm live orders.
    OrderResponse(String),
    /// Three consecutive transient maker-cycle failures.
    ConsecutiveErrors(String),
    /// Venue position cannot be explained by fills owned by this run.
    PositionReconciliation(String),
}

impl MakerExit {
    fn lifecycle_reason(&self) -> String {
        match self {
            Self::CtrlC => "Ctrl+C".to_string(),
            Self::OrderResponse(error) => {
                format!("fail-safe: order-response stream unavailable: {error}")
            }
            Self::ConsecutiveErrors(error) => {
                format!("fail-safe: 3 consecutive maker cycle errors: {error}")
            }
            Self::PositionReconciliation(error) => {
                format!("fail-safe: position reconciliation failed: {error}")
            }
        }
    }

    fn terminal_error(&self) -> Option<String> {
        match self {
            Self::CtrlC => None,
            Self::OrderResponse(error) => Some(format!(
                "maker stopped immediately (fail-safe): order-response stream unavailable: {error}"
            )),
            Self::ConsecutiveErrors(error) => Some(format!(
                "maker stopped after 3 consecutive maker cycle errors (fail-safe): {error}"
            )),
            Self::PositionReconciliation(error) => Some(format!(
                "maker stopped immediately (fail-safe): position reconciliation failed: {error}"
            )),
        }
    }
}

